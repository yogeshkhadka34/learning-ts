"use strict";
//for in
let colors = ["red", "green", "blue"];
for (let color in colors) {
    console.log(colors[color]);
}
//for of
for (let index of colors) {
    console.log(index);
}
//splitting letters of string by for of
let letters = "ABC";
for (let letter of letters) {
    console.log(letter);
}
